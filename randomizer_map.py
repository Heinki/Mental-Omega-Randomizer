def now_stamp():
    return datetime.now().strftime('%Y%m%d-%H%M%S')


def backup_file_once(path, label):
    if not path.exists():
        return None

    BACKUP_DIR.mkdir(parents=True, exist_ok=True)
    target = BACKUP_DIR / f'{path.name}.{label}.bak'
    if not target.exists():
        shutil.copy2(path, target)
    return target


def is_generated_rules_file(path):
    if not path.exists():
        return False
    try:
        return read_text(path).startswith(RANDOMIZER_RULES_MARKER)
    except OSError:
        return False


def is_generated_hooked_map(path):
    if not path.exists():
        return False
    try:
        with path.open('r', encoding='utf-8', errors='ignore') as handle:
            return handle.readline().strip() == HOOKED_MAP_MARKER
    except OSError:
        return False


def sanitize_marker_part(value, max_length=12):
    cleaned = re.sub(r'[^A-Za-z0-9]', '', value or '').upper()
    return (cleaned or 'UNK')[:max_length]


def tech_ids_for_rewards(rewards):
    tech_ids = set()
    for reward in rewards:
        reward = canonical_reward(reward)
        for section, values in reward.get('rules', {}).items():
            if any(key.lower() == 'techlevel' for key in values):
                tech_ids.add(section.upper())
    return tech_ids


def unlocked_reward_tech_ids(rewards):
    tech_ids = set()
    for reward in canonical_rewards(rewards):
        if reward.get('kind') == 'buff':
            continue
        tech_ids.update(tech_ids_for_rewards([reward]))
    return tech_ids


def buffs_with_unlocked_access(
    rewards,
    require_unlocked_access=True,
    additional_unlocked_tech_ids=None,
    share_basic_equivalent_buffs=False,
):
    unlocked = unlocked_reward_tech_ids(rewards)
    unlocked.update(
        str(unit_id).upper()
        for unit_id in (additional_unlocked_tech_ids or [])
    )
    filtered = []
    for reward in canonical_rewards(rewards):
        if reward.get('kind') != 'buff':
            filtered.append(reward)
            continue
        unit_id = (reward.get('unit') or '').upper()
        equivalents = unit_role_equivalents(unit_id)
        equivalent_is_unlocked = (
            share_basic_equivalent_buffs
            and len(equivalents) > 1
            and bool(unlocked.intersection(equivalents))
        )
        if (
            not require_unlocked_access
            or reward.get('global_buff')
            or not unit_id
            or unit_id in unlocked
            or equivalent_is_unlocked
        ):
            filtered.append(reward)
    return filtered


def expand_equivalent_role_buffs(rewards, enabled=False):
    """Apply each active unit buff to explicit cross-faction role peers."""
    if not enabled:
        return list(rewards)
    expanded = []
    for reward in rewards:
        expanded.append(reward)
        if reward.get('kind') != 'buff' or reward.get('mission_assistance'):
            continue
        for unit_id in sorted(unit_role_equivalents(reward.get('unit'))):
            if unit_id == reward.get('unit'):
                continue
            equivalent = dict(reward)
            equivalent['unit'] = unit_id
            expanded.append(equivalent)
    return expanded


def remove_locked_techlevel_actions(lines, unlocked_tech_ids, randomized_tech_ids=None):
    start, end = find_section_bounds(lines, 'Actions')
    if start is None:
        return 0

    controlled = {
        section.upper()
        for section in (
            controlled_tech_ids() if randomized_tech_ids is None else randomized_tech_ids
        )
    }
    unlocked = {section.upper() for section in unlocked_tech_ids}
    removed = 0

    for index in range(start + 1, end):
        line = lines[index]
        if '=' not in line:
            continue

        key, value = line.split('=', 1)
        count, groups = parse_action_groups(value)
        if not count or not groups:
            continue

        kept = []
        for group in groups:
            if len(group) >= 3 and group[0] == '106':
                tech_id = group[2].strip().upper()
                if tech_id in controlled and tech_id not in unlocked:
                    removed += 1
                    continue
            kept.append(group)

        if len(kept) != len(groups):
            tokens = action_group_tokens(kept)
            lines[index] = f'{key.strip()}={len(kept)}' + (',' + ','.join(tokens) if tokens else '')

    return removed


def action_has_code(groups, code):
    return any(group and group[0] == str(code) for group in groups)


def action_has_objective_complete(groups):
    for group in groups:
        if len(group) < 3:
            continue
        action_code = group[0]
        parameter = group[2].lower()
        if action_code == '19' and parameter == 'objectivecomplete':
            return True
        if action_code == '21' and parameter == 'eva_objectivecomplete':
            return True
        if action_code == '11' and parameter == 'mission:objc':
            return True
    return False


def superweapon_actions_for_rewards(rewards):
    actions = []
