def apply_unit_buff_value(values, target, buff_type, count):
    if buff_type == 'health':
        multiplier = min(2.0, 1.15 ** count)
        values['Strength'] = str(max(1, int(round(target['strength'] * multiplier))))
    elif buff_type == 'sight':
        values['Sight'] = str(int(round(target['sight'] + min(4, count))))
    elif buff_type == 'ammo':
        values['Ammo'] = str(int(round(target['ammo'] + min(5, count))))
    elif buff_type == 'self_healing':
        values['SelfHealing'] = 'yes'
    elif buff_type == 'cloak':
        values['Cloakable'] = 'yes'
        values['Cloakable.Stages'] = '1'
        values['CloakingSpeed'] = '1'
        values['CloakSound'] = 'none'
    elif buff_type == 'sensors':
        values['Sensors'] = 'yes'
        values['SensorsSight'] = str(int(round(target.get('sight', 5) + 2)))
    elif buff_type == 'cost':
        multiplier = max(0.30, 0.80 ** count)
        values['Cost'] = str(max(1, int(round(target['cost'] * multiplier))))
    elif buff_type == 'speed':
        multiplier = min(1.75, 1.10 ** count)
        values['Speed'] = str(max(1, int(round(target['speed'] * multiplier))))
    elif buff_type == 'armor':
        multiplier = max(0.50, 0.90 ** count)
        current_strength = int(values.get('Strength', target['strength']))
        values['Strength'] = str(max(1, int(round(current_strength / multiplier))))
    else:
        return False
    return True


def apply_weapon_buff_value(values, base_stats, buff_type, count):
    if buff_type == 'damage' and base_stats.get('damage', 0) > 0:
        multiplier = min(2.0, 1.15 ** count)
        base_damage = int(round(base_stats['damage']))
        values['Damage'] = str(max(base_damage + 1, int(round(base_damage * multiplier))))
    elif buff_type == 'range' and base_stats.get('range', 0) > 0:
        values['Range'] = format_multiplier(base_stats['range'] + min(3.0, 0.5 * count))
    elif buff_type == 'reload' and base_stats.get('rof', 0) > 1:
        multiplier = max(0.45, 0.90 ** count)
        values['ROF'] = str(max(1, int(round(base_stats['rof'] * multiplier))))
    else:
        return False
    return True


def unit_weapon_buff_rules(
    lines,
    rewards,
    buff_allied_helpers=False,
    require_unlocked_access=True,
    additional_unlocked_tech_ids=None,
    share_basic_equivalent_buffs=False,
    unit_specific_mode=False,
):
    """Apply direct buffs only when their global type is safe for friendly houses.

    TechnoType and WeaponType values are global in the engine. Keep the
    established safety guard here: player, allied-helper, and transfer houses
    participate; a direct buff is skipped if an enemy also uses the affected
    global type.
    """
    records = map_house_records(lines)
    player_house = player_house_from_map(lines, records=records)
    if not player_house:
        return ({}, [], [])

    player_houses = player_controlled_houses(lines, records=records) or [player_house]
    scripted_enemies = scripted_enemy_house_pairs(lines, records=records)
    transfer_houses = player_transfer_houses(
        lines,
        records=records,
        scripted_enemies=scripted_enemies,
    )
    helper_houses = (
        allied_helper_houses(
            lines,
            player_house,
            records=records,
            transfer_houses=transfer_houses,
            scripted_enemies=scripted_enemies,
        )
        if buff_allied_helpers else []
    )
    allowed_names = []
    for house in unique_in_order(player_houses + helper_houses + transfer_houses):
        record = records.get(house, {})
        if not record:
            record = records.get(house + ' House', {})
        allowed_names.append(house)
        allowed_names.append(house.replace(' House', ''))
        if not house.lower().endswith(' house'):
            allowed_names.append(house + ' House')
        if record.get('country'):
            allowed_names.append(record['country'])
    allowed_houses = {name.lower() for name in allowed_names if name}
    usage_index = build_unit_usage_index(lines)

    grouped_counts = {}
    active_rewards = buffs_with_unlocked_access(
        rewards,
        require_unlocked_access=require_unlocked_access,
        additional_unlocked_tech_ids=additional_unlocked_tech_ids,
        share_basic_equivalent_buffs=share_basic_equivalent_buffs,
    )
    for reward in expand_equivalent_role_buffs(
        active_rewards,
        enabled=share_basic_equivalent_buffs,
    ):
        if reward.get('kind') != 'buff':
            continue
        buff_type = reward.get('buff_type')
        direct_chaos_types = {'cost', 'speed', 'armor'} if unit_specific_mode else set()
        if buff_type not in CLONE_REQUIRED_BUFF_TYPES and buff_type not in direct_chaos_types:
            continue
        unit_id = reward.get('unit')
        target = BUFF_TARGETS.get(unit_id, {})
        if not unit_id or not target:
            continue
        if (
            buff_type in WEAPON_STAT_BUFF_TYPES
            and not target.get('weapons')
            and not (buff_type == 'damage' and target.get('special_damage_fields'))
        ):
            continue
        if buff_type == 'health' and 'strength' not in target:
            continue
        if buff_type == 'sight' and 'sight' not in target:
            continue
        if buff_type == 'ammo' and 'ammo' not in target:
            continue
        if buff_type == 'cost' and 'cost' not in target:
            continue
        if buff_type == 'speed' and 'speed' not in target:
            continue
        if buff_type == 'armor' and 'strength' not in target:
            continue
        key = (unit_id, buff_type)
        grouped_counts[key] = grouped_counts.get(key, 0) + 1

    counts_by_unit = {}
    for (unit_id, buff_type), count in grouped_counts.items():
        counts_by_unit.setdefault(unit_id, {})[buff_type] = count

    rule_sections = {}
    applied_units = []
    skipped_units = []
    for unit_id, counts in counts_by_unit.items():
        target = BUFF_TARGETS.get(unit_id, {})
        unsafe_unit_houses = sorted({
            house
            for house in unit_usage_houses(lines, unit_id, usage_index)
            if house.lower() not in allowed_houses
        })

        applied = False
        direct_types = set(counts) - WEAPON_STAT_BUFF_TYPES
        if unsafe_unit_houses and direct_types:
            skipped_units.append(
                f'{target.get("label", unit_id)} ({", ".join(unsafe_unit_houses)})'
            )
        elif direct_types:
            unit_values = rule_sections.setdefault(unit_id, {})
            for buff_type in (
                'health', 'armor', 'sight', 'ammo', 'self_healing', 'cloak',
                'sensors', 'cost', 'speed',
            ):
                if buff_type in counts and apply_unit_buff_value(
                    unit_values,
                    target,
                    buff_type,
                    counts[buff_type],
                ):
                    applied = True
            if not unit_values:
                rule_sections.pop(unit_id, None)

        weapon_buff_types = WEAPON_STAT_BUFF_TYPES.intersection(counts)
        if weapon_buff_types:
            for weapon, base_stats in target.get('weapons', {}).items():
                weapon_users = SHARED_WEAPON_USER_IDS.get(weapon.upper(), {unit_id})
                unsafe_weapon_houses = sorted({
                    house
                    for weapon_user in weapon_users
                    for house in unit_usage_houses(lines, weapon_user, usage_index)
                    if house.lower() not in allowed_houses
                })
                if unsafe_weapon_houses:
                    skipped_units.append(
                        f'{target.get("label", unit_id)} / {weapon} '
                        f'({", ".join(unsafe_weapon_houses)})'
                    )
                    continue
                weapon_values = {}
                for buff_type in ('damage', 'range', 'reload'):
                    if buff_type in weapon_buff_types:
                        applied = (
                            apply_weapon_buff_value(
                                weapon_values,
                                base_stats,
                                buff_type,
                                counts[buff_type],
                            )
                            or applied
                        )
                if not weapon_values:
                    continue
                rule_sections.setdefault(weapon, {}).update(weapon_values)
        if 'damage' in weapon_buff_types and target.get('special_damage_fields'):
            if unsafe_unit_houses:
                skipped_units.append(
                    f'{target.get("label", unit_id)} / spawned missile damage '
                    f'({", ".join(unsafe_unit_houses)})'
                )
            else:
                multiplier = min(2.0, 1.15 ** counts['damage'])
                general_values = rule_sections.setdefault('General', {})
                for field, base_damage in target['special_damage_fields'].items():
                    general_values[field] = str(
                        max(base_damage + 1, int(round(base_damage * multiplier)))
                    )
                applied = True
        if applied:
            applied_units.append(target.get('label', unit_id))

    return (rule_sections, unique_in_order(applied_units), unique_in_order(skipped_units))


