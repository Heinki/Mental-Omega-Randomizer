def parse_float(value, default=1.0):
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def format_multiplier(value):
    return f'{value:.3f}'.rstrip('0').rstrip('.')


def comma_items(value):
    return [item.strip() for item in (value or '').split(',') if item.strip()]


def merge_unique_csv(existing, additions):
    merged = []
    seen = set()
    for item in comma_items(existing) + list(additions):
        key = item.upper()
        if key in seen:
            continue
        seen.add(key)
        merged.append(item)
    return ','.join(merged)


def stacked_house_buff_values(
    rewards,
    base_values=None,
    require_unlocked_access=True,
    additional_unlocked_tech_ids=None,
    share_basic_equivalent_buffs=False,
    unit_specific_mode=False,
):
    base_values = base_values or {}
    category_counts = {}
    veteran_units = {}
    rof_count = 0

    for reward in buffs_with_unlocked_access(
        rewards,
        require_unlocked_access=require_unlocked_access,
        additional_unlocked_tech_ids=additional_unlocked_tech_ids,
        share_basic_equivalent_buffs=share_basic_equivalent_buffs,
    ):
        if reward.get('kind') != 'buff' or not reward.get('unit') or not reward.get('buff_type'):
            continue
        buff_type = reward.get('buff_type')
        if buff_type not in HOUSE_SCOPED_BUFF_TYPES:
            continue
        target = BUFF_TARGETS.get(reward.get('unit'))
        if not target:
            continue
        if (
            unit_specific_mode
            and buff_type in {'production', 'cost', 'speed', 'armor'}
            and not target.get('global_production')
        ):
            continue
        if buff_type == 'production' and target.get('global_production'):
            for global_suffix in ('Infantry', 'Units', 'Aircraft', 'Buildings', 'Defenses'):
                key = (buff_type, global_suffix)
                category_counts[key] = category_counts.get(key, 0) + 1
            continue
        suffix = house_category_suffix(target)
        if buff_type == 'rof':
            rof_count += 1
            continue
        if buff_type == 'veteran':
            # Ares calls the country flag VeteranBuildings, including for
            # defenses. VeteranDefenses is not an engine key.
            if target.get('category') == 'defenses':
                if not target.get('trainable'):
                    continue
                suffix = 'Buildings'
            units = (
                sorted(unit_role_equivalents(reward['unit']))
                if share_basic_equivalent_buffs
                else [reward['unit']]
            )
            for unit_id in units:
                equivalent_target = BUFF_TARGETS.get(unit_id, target)
                if (
                    equivalent_target.get('category') == 'defenses'
                    and not equivalent_target.get('trainable')
                ):
                    continue
                # Ares uses VeteranBuildings for trainable defenses. There is
                # no VeteranDefenses country key.
                equivalent_suffix = (
                    'Buildings'
                    if equivalent_target.get('category') == 'defenses'
                    else house_category_suffix(equivalent_target)
                )
                veteran_units.setdefault(equivalent_suffix, []).append(unit_id)
            continue
        suffixes = {suffix}
        if share_basic_equivalent_buffs:
            suffixes.update(
                house_category_suffix(BUFF_TARGETS[unit_id])
                for unit_id in unit_role_equivalents(reward['unit'])
                if unit_id in BUFF_TARGETS
            )
        for equivalent_suffix in suffixes:
            key = (buff_type, equivalent_suffix)
            category_counts[key] = category_counts.get(key, 0) + 1

    values = {}
    for (buff_type, suffix), count in category_counts.items():
        if buff_type == 'production':
            key = f'BuildTime{suffix}Mult'
            multiplier = max(0.35, 0.85 ** count)
        elif buff_type == 'cost':
            key = f'Cost{suffix}Mult'
            multiplier = max(0.30, 0.80 ** count)
        elif buff_type == 'speed':
            key = f'Speed{suffix}Mult'
            multiplier = min(1.75, 1.10 ** count)
        elif buff_type == 'armor':
            key = f'Armor{suffix}Mult'
            multiplier = max(0.50, 0.90 ** count)
        else:
            continue
        existing_key = next((key_name for key_name in base_values if key_name.lower() == key.lower()), key)
        base = parse_float(base_values.get(existing_key), 1.0)
        values[key] = format_multiplier(base * multiplier)

    if rof_count:
        base = parse_float(base_values.get('ROF'), 1.0)
        values['ROF'] = format_multiplier(base * max(0.40, 0.90 ** rof_count))

    for suffix, units in veteran_units.items():
        key = f'Veteran{suffix}'
        existing_key = next((key_name for key_name in base_values if key_name.lower() == key.lower()), key)
        values[key] = merge_unique_csv(base_values.get(existing_key, ''), units)

    return values


def mission_assistance_multipliers(stacks):
    """Return the cumulative player-only multipliers for failed-mission retries."""
    try:
        stacks = max(0, int(stacks))
    except (TypeError, ValueError):
        stacks = 0
    return {
        'production': max(0.35, 0.85 ** stacks),
        'cost': max(0.30, 0.80 ** stacks),
        'speed': min(1.75, 1.10 ** stacks),
        'armor': max(0.50, 0.90 ** stacks),
        'rof': max(0.40, 0.90 ** stacks),
        'health': min(2.0, 1.15 ** stacks),
        'damage': min(2.0, 1.15 ** stacks),
        'range': min(3.0, 0.5 * stacks),
    }


def mission_assistance_direct_rewards(unit_ids, stacks):
    """Build guarded health/damage/range buffs for accessible retry units.

    These stats live on global TechnoType/WeaponType sections rather than a
    country.  ``unit_weapon_buff_rules`` therefore remains responsible for
    rejecting a type whenever a non-assisted house uses it in this map.
    """
    try:
        stacks = max(0, int(stacks))
    except (TypeError, ValueError):
        stacks = 0
    rewards = []
    if not stacks:
        return rewards
    direct_stacks = min(stacks, 6)
    for unit_id in unique_in_order(
        str(unit_id or '').upper() for unit_id in (unit_ids or [])
    ):
        if unit_id not in BUFF_TARGETS:
            continue
        for _ in range(direct_stacks):
            for buff_type in ('health', 'damage', 'range'):
                rewards.append({
                    'kind': 'buff',
                    'unit': unit_id,
                    'buff_type': buff_type,
                    'global_buff': True,
                    'mission_assistance': True,
                })
    return rewards


def mission_assistance_buff_values(base_values, stacks):
    """Build category/country overrides for one mission's assistance stacks."""
    try:
        stacks = max(0, int(stacks))
    except (TypeError, ValueError):
        stacks = 0
    multipliers = mission_assistance_multipliers(stacks)
    if not stacks:
        return {}

    values = {}
    fields = (
        ('BuildTime', 'production'),
        ('Cost', 'cost'),
        ('Speed', 'speed'),
        ('Armor', 'armor'),
    )
    for prefix, multiplier_name in fields:
        for suffix in MISSION_ASSISTANCE_CATEGORIES:
            key = f'{prefix}{suffix}Mult'
            existing_key = next(
                (key_name for key_name in base_values if key_name.lower() == key.lower()),
                key,
            )
            base = parse_float(base_values.get(existing_key), 1.0)
            values[key] = format_multiplier(base * multipliers[multiplier_name])

    rof_key = next((key for key in base_values if key.lower() == 'rof'), 'ROF')
    values['ROF'] = format_multiplier(parse_float(base_values.get(rof_key), 1.0) * multipliers['rof'])
    return values


def mission_assistance_buff_rules(lines, stacks, buff_allied_helpers=False):
    """Scope retry assistance without changing a campaign house's country.

    Trigger owners in campaign maps are country IDs (for example ``Guild1``
    or ``UnitedStates``). Reassigning a house to a private clone disconnects
    every trigger owned by the original country and can break scripted unit
    transfers or the mission itself. Country-level assistance is therefore
    applied only when every house in that country family is an assisted house.
    Global unit/weapon assistance is guarded separately by
    :func:`unit_weapon_buff_rules`.
    """
    try:
        stacks = max(0, int(stacks))
    except (TypeError, ValueError):
        stacks = 0
    if not stacks:
        return ({}, [], [])

    records = map_house_records(lines)
    primary_house = player_house_from_map(lines, records=records)
    if not primary_house:
        return ({}, [], [])
    player_houses = player_controlled_houses(lines, records=records) or [primary_house]
    scripted_enemies = scripted_enemy_house_pairs(lines, records=records)
    transfer_houses = player_transfer_houses(
        lines,
        records=records,
        scripted_enemies=scripted_enemies,
    )
    helper_houses = (
        allied_helper_houses(
            lines,
            primary_house,
            records=records,
            transfer_houses=transfer_houses,
            scripted_enemies=scripted_enemies,
        )
        if buff_allied_helpers
        else []
    )
    assisted_houses = unique_in_order(player_houses + transfer_houses + helper_houses)
    usage_index = build_unit_usage_index(lines)
    country_houses = {}
    for house in assisted_houses:
        country = records.get(house, {}).get('country') or house.replace(' House', '')
        country_houses.setdefault(country, []).append(house)

    rule_sections = {}
    skipped_countries = []
    for country, houses in country_houses.items():
        original_values = section_value_map_preserve(lines, country)
        assistance_values = mission_assistance_buff_values(original_values, stacks)
        shared_houses = unsafe_country_houses(
            lines,
            country,
            assisted_houses,
            records=records,
            usage_index=usage_index,
            scripted_enemies=scripted_enemies,
        )
        if shared_houses:
            skipped_countries.append((country, list(houses), shared_houses))
            continue
        rule_sections[country] = assistance_values

    return (rule_sections, assisted_houses, skipped_countries)


def ai_trigger_team_usage_houses(lines):
    """Return the runtime owner overrides for TeamTypes used by AI triggers.

    FinalAlert commonly stores reusable TeamTypes with ``House=Neutral`` and
    supplies the real owner in ``[AITriggerTypes]``. Treating that placeholder
    as a live Neutral team incorrectly rejects friendly global unit buffs.
    """
    team_houses = {}
    for line in section_lines(lines, 'AITriggerTypes'):
        if '=' not in line:
            continue
        _, value = line.split('=', 1)
        tokens = [token.strip() for token in value.split(',')]
        if len(tokens) < 3:
            continue
        team_id = tokens[1]
        house = tokens[2]
        if not team_id or team_id.lower() in {'<none>', 'none'}:
            continue
        if not house or house.lower() in {'<none>', 'none'}:
            continue
        team_houses.setdefault(team_id.lower(), set()).add(house)
    return team_houses


def directly_created_team_ids(lines):
    """Return TeamTypes created directly by map actions instead of AI triggers."""
    team_ids = set()
    for line in section_lines(lines, 'Actions'):
        if '=' not in line:
            continue
        _, value = line.split('=', 1)
        _, groups = parse_action_groups(value)
        for group in groups:
            # Action 4 is Create Team; parameter 3 contains the TeamType ID.
            if group[0] == '4' and group[2]:
                team_ids.add(group[2].lower())
    return team_ids


def taskforce_usage_houses(lines, sections=None):
    """Resolve each TaskForce to the houses that can own it at runtime."""
    sections = sections or all_section_value_maps(lines)
    ai_team_houses = ai_trigger_team_usage_houses(lines)
    directly_created = directly_created_team_ids(lines)
    taskforce_to_houses = {}
    placeholder_houses = {'neutral', 'neutral house', '<none>', 'none'}
    for section_name, values in sections.items():
        taskforce = values.get('taskforce')
        house = values.get('house')
        if not taskforce or not house:
            continue

        section_key = section_name.lower()
        runtime_houses = ai_team_houses.get(section_key, set())
        houses = taskforce_to_houses.setdefault(taskforce.lower(), set())
        houses.update(runtime_houses)

        # A Neutral TeamType is a template when AITriggerTypes supplies the
        # actual owner. Keep Neutral only when the team can also be created
        # directly, or when no runtime owner override exists.
        if (
            house.lower() not in placeholder_houses
            or not runtime_houses
            or section_key in directly_created
        ):
            houses.add(house)
    return taskforce_to_houses


def taskforce_unit_usage_houses(lines, unit_id):
    unit_upper = (unit_id or '').upper()
    sections = all_section_value_maps(lines)
    sections_by_lower = {name.lower(): values for name, values in sections.items()}
    taskforce_to_houses = taskforce_usage_houses(lines, sections=sections)

    usage_houses = set()
    for taskforce_id, houses in taskforce_to_houses.items():
        for value in sections_by_lower.get(taskforce_id, {}).values():
            tokens = [token.strip() for token in value.split(',')]
            if len(tokens) >= 2 and tokens[1].upper() == unit_upper:
                usage_houses.update(houses)
    return usage_houses


def placed_unit_usage_houses(lines, unit_id):
    unit_upper = (unit_id or '').upper()
    usage_houses = set()
    for section in ('Infantry', 'Units', 'Aircraft', 'Structures'):
        for line in section_lines(lines, section):
            if '=' not in line:
                continue
            _, value = line.split('=', 1)
            tokens = [token.strip() for token in value.split(',')]
            if len(tokens) >= 2 and tokens[1].upper() == unit_upper:
                usage_houses.add(tokens[0])
    return usage_houses


def build_unit_usage_index(lines):
    """Index placed and scripted unit ownership with one map parse.

    Direct buffs may inspect hundreds of unit and shared-weapon users. Parsing
    every large campaign map once per lookup caused multi-second launch stalls.
    The index preserves the same ownership rules while making later lookups
    constant-time.
    """
    usage = {}
    sections = all_section_value_maps(lines)
    sections_by_lower = {name.lower(): values for name, values in sections.items()}
    for section in ('Infantry', 'Units', 'Aircraft', 'Structures'):
        for value in sections_by_lower.get(section.lower(), {}).values():
            tokens = [token.strip() for token in value.split(',')]
            if len(tokens) >= 2:
                usage.setdefault(tokens[1].upper(), set()).add(tokens[0])

    for taskforce_id, houses in taskforce_usage_houses(lines, sections=sections).items():
        for value in sections_by_lower.get(taskforce_id, {}).values():
            tokens = [token.strip() for token in value.split(',')]
            if len(tokens) >= 2:
                usage.setdefault(tokens[1].upper(), set()).update(houses)
    return usage


def unit_usage_houses(lines, unit_id, usage_index=None):
    if usage_index is not None:
        return set(usage_index.get(str(unit_id or '').upper(), set()))
    return placed_unit_usage_houses(lines, unit_id) | taskforce_unit_usage_houses(lines, unit_id)


def mission_assistance_unit_ids(
    lines,
    unlocked_unit_ids=None,
    additional_unit_ids=None,
    randomized_access=True,
    fallback_faction='',
    buff_allied_helpers=False,
):
    """Return units the player can use through progression or this mission.

    Randomized-access runs use earned/always-available units plus units placed
    or scripted for the player in this map. Buffs-only runs additionally show
    the complete normal roster for the player faction.
    """
    combat_categories = {'infantry', 'units', 'aircraft'}

    def is_unit(unit_id):
        return BUFF_TARGETS.get(str(unit_id or '').upper(), {}).get('category') in combat_categories

    records = map_house_records(lines)
    player_houses = player_controlled_houses(lines, records=records)
    primary_house = player_house_from_map(lines, records=records)
    if not player_houses and primary_house:
        player_houses = [primary_house]
    scripted_enemies = scripted_enemy_house_pairs(lines, records=records)
    transfer_houses = player_transfer_houses(
        lines,
        records=records,
        scripted_enemies=scripted_enemies,
    )
    helper_houses = (
        allied_helper_houses(
            lines,
            primary_house,
            records=records,
            transfer_houses=transfer_houses,
            scripted_enemies=scripted_enemies,
        )
        if buff_allied_helpers and primary_house
        else []
    )

    allowed_names = set()
    for house in unique_in_order(player_houses + helper_houses + transfer_houses):
        record = records.get(house, {})
        allowed_names.update({
            house.lower(),
            house.replace(' House', '').lower(),
            (record.get('country') or '').lower(),
        })
    allowed_names.discard('')

    unit_ids = {
        str(unit_id).upper()
        for unit_id in (unlocked_unit_ids or [])
        if is_unit(unit_id)
    }
    unit_ids.update(
        str(unit_id).upper()
        for unit_id in (additional_unit_ids or [])
        if is_unit(unit_id)
    )

    # Include units already placed for the player. This is especially
    # important for the first mission, before any access rewards exist.
    for section in ('Infantry', 'Units', 'Aircraft'):
        for line in section_lines(lines, section):
            if '=' not in line:
                continue
            _, value = line.split('=', 1)
            tokens = [token.strip() for token in value.split(',')]
            if len(tokens) >= 2 and tokens[0].lower() in allowed_names and is_unit(tokens[1]):
                unit_ids.add(tokens[1].upper())

    # Include player-owned TaskForce units used by reinforcements and other
    # scripted teams, without repeatedly reparsing the whole map per roster ID.
    sections = all_section_value_maps(lines)
    sections_by_lower = {name.lower(): values for name, values in sections.items()}
    taskforce_owners = taskforce_usage_houses(lines, sections=sections)
    for taskforce, owners in taskforce_owners.items():
        if not {owner.lower() for owner in owners}.intersection(allowed_names):
            continue
        for value in sections_by_lower.get(taskforce, {}).values():
            tokens = [token.strip() for token in value.split(',')]
            if len(tokens) >= 2 and is_unit(tokens[1]):
                unit_ids.add(tokens[1].upper())

    family_names = {
        'allies': 'Allies',
        'soviets': 'Soviets',
        'epsilon': 'Epsilon',
        'foehn': 'Foehn',
    }
    player_factions = set()
    for house in player_houses:
        family = country_family(records.get(house, {}))
        if family in family_names:
            player_factions.add(family_names[family])
    if fallback_faction in FACTION_UNIT_ROSTERS:
        player_factions.add(fallback_faction)

    for faction in player_factions:
        roster_ids = {
            unit_id.upper()
            for category in FACTION_UNIT_ROSTERS.get(faction, {}).values()
            for unit_id in category
        }
        if randomized_access:
            roster_ids.intersection_update(ALWAYS_AVAILABLE_UNIT_IDS)
        unit_ids.update(unit_id for unit_id in roster_ids if is_unit(unit_id))

    return sorted(unit_ids)


