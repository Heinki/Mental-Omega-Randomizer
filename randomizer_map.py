def append_action_to_action_id(lines, action_id, action_tokens):
    start, end = find_section_bounds(lines, 'Actions')
    if start is None:
        return False

    for index in range(start + 1, end):
        line = lines[index]
        if '=' not in line:
            continue
        key, value = line.split('=', 1)
        if key.strip().lower() != action_id.lower():
            continue
        tokens = [token.strip() for token in value.split(',')]
        try:
            tokens[0] = str(int(tokens[0]) + 1)
        except (ValueError, IndexError):
            return False
        tokens.extend(action_tokens)
        replacement = f'{key}={",".join(tokens)}'
        if len(replacement.encode('utf-8')) > MAX_MAP_ACTION_LINE_LENGTH:
            return False
        lines[index] = replacement
        return True
    return False


def insert_actions_before_codes(lines, action_id, action_tokens_list, before_codes):
    """Insert trigger actions before the first matching terminal action.

    Winner/announce-win actions can end the scenario before later actions in
    the same action list run.  Victory markers therefore need to be inserted
    before those actions instead of appended to the list.
    """
    actions = [list(tokens) for tokens in action_tokens_list]
    if not actions or any(len(tokens) != 8 for tokens in actions):
        return False

    wanted_codes = {str(code) for code in before_codes}
    start, end = find_section_bounds(lines, 'Actions')
    if start is None:
        return False

    for index in range(start + 1, end):
        line = lines[index]
        if '=' not in line:
            continue
        key, value = line.split('=', 1)
        if key.strip().lower() != action_id.lower():
            continue

        tokens = [token.strip() for token in value.split(',')]
        try:
            declared_count = int(tokens[0])
        except (ValueError, IndexError):
            return False

        insert_group = None
        for group_index in range(min(declared_count, (len(tokens) - 1) // 8)):
            if tokens[1 + group_index * 8] in wanted_codes:
                insert_group = group_index
                break
        if insert_group is None:
            return False

        insert_at = 1 + insert_group * 8
        inserted_tokens = [token for action in actions for token in action]
        tokens[insert_at:insert_at] = inserted_tokens
        tokens[0] = str(declared_count + len(actions))
        replacement = f'{key}={",".join(tokens)}'
        if len(replacement.encode('utf-8')) > MAX_MAP_ACTION_LINE_LENGTH:
            return False
        lines[index] = replacement
        return True
    return False


def append_parallel_global_hook(lines, source_action_id, marker_action, marker_name):
    """Mirror a global objective trigger when its native action line is full.

    Action lines are capped at 511 bytes, but some campaign objective lists
    already sit near that limit. Global-variable and all-of-type-destroyed
    events can be observed by a second trigger. Mirror native enable/disable
    actions so a source trigger that starts disabled retains its timing.
    """
    events = section_value_map_preserve(lines, 'Events')
    triggers = section_value_map_preserve(lines, 'Triggers')
    source_event = events.get(source_action_id)
    source_trigger = triggers.get(source_action_id)
    if not source_event or not source_trigger or len(marker_action) != 8:
        return False

    event_tokens = [token.strip() for token in source_event.split(',')]
    if len(event_tokens) < 2 or event_tokens[0] != '1' or event_tokens[1] not in {'11', '61'}:
        return False

    trigger_tokens = [token.strip() for token in source_trigger.split(',')]
    if len(trigger_tokens) != 8:
        return False

    hook_trigger_id = unique_section_key(lines, ('Events', 'Actions', 'Triggers'), 'RNH00')
    hook_tag_id = unique_section_key(lines, ('Tags',), 'RHT00')
    source_lower = source_action_id.lower()
    mirrored_lines = []
    mirrored_enable = False
    start, end = find_section_bounds(lines, 'Actions')
    if start is None:
        return False

    for index in range(start + 1, end):
        line = lines[index]
        if '=' not in line:
            continue
        key, value = line.split('=', 1)
        declared_count, groups = parse_action_groups(value)
        if declared_count != len(groups):
            continue

        expanded = []
        changed = False
        for group in groups:
            expanded.append(group)
            if group[0] not in {'53', '54'} or group[2].lower() != source_lower:
                continue
            mirrored = list(group)
            mirrored[2] = hook_trigger_id
            expanded.append(mirrored)
            changed = True
            if group[0] == '53':
                mirrored_enable = True
        if not changed:
            continue

        replacement = f'{key}={len(expanded)},{",".join(action_group_tokens(expanded))}'
        if len(replacement.encode('utf-8')) > MAX_MAP_ACTION_LINE_LENGTH:
            return False
        mirrored_lines.append((index, replacement))

    source_starts_disabled = trigger_tokens[3] == '1'
    if source_starts_disabled and not mirrored_enable:
        return False

    for index, replacement in mirrored_lines:
        lines[index] = replacement

    trigger_tokens[2] = marker_name
    append_section_entry(lines, 'Events', hook_trigger_id, source_event)
    append_section_entry(lines, 'Actions', hook_trigger_id, f'1,{",".join(marker_action)}')
    append_section_entry(lines, 'Triggers', hook_trigger_id, ','.join(trigger_tokens))
    append_section_entry(lines, 'Tags', hook_tag_id, f'0,{marker_name} 1,{hook_trigger_id}')
    return True


def unique_section_key(lines, sections, prefix):
    existing = set()
    for section in sections:
        for line in section_lines(lines, section):
            if '=' in line:
                existing.add(line.split('=', 1)[0].strip().lower())
    for index in range(1, 1000):
        candidate = f'{prefix}{index:03d}'
        if candidate.lower() not in existing:
            return candidate
    raise RuntimeError(f'Could not allocate a unique {prefix} map key.')


SUPERWEAPON_ACTIONS_PER_TRIGGER = 16


def append_superweapon_grant_trigger(lines, house, action_groups):
    """Grant earned powers without overflowing a campaign action list.

    Mental Omega's installed campaign maps top out at 24 actions in one list.
    Large Chaos inventories previously wrote every power into a single list
    (35 in the reported crash), which corrupts the engine while processing the
    map-start trigger. Keep a conservative margin and stagger additional
    chunks by one second so every power is still granted exactly once.
    """
    actions = [list(group) for group in action_groups]
    if not actions or any(len(group) != 8 for group in actions):
        return ''

    trigger_ids = []
    for offset in range(0, len(actions), SUPERWEAPON_ACTIONS_PER_TRIGGER):
        chunk = actions[offset:offset + SUPERWEAPON_ACTIONS_PER_TRIGGER]
        chunk_number = len(trigger_ids) + 1
        trigger_id = unique_section_key(lines, ('Events', 'Actions', 'Triggers'), 'RNGSW')
        tag_id = unique_section_key(lines, ('Tags',), 'RNGST')
        action_tokens = ','.join(action_group_tokens(chunk))
        name = f'MOR Earned Superweapons {chunk_number}'
        append_section_entry(lines, 'Events', trigger_id, f'1,13,0,{chunk_number}')
        append_section_entry(lines, 'Actions', trigger_id, f'{len(chunk)},{action_tokens}')
        append_section_entry(lines, 'Triggers', trigger_id, f'{house},<none>,{name},0,1,1,1,0')
        append_section_entry(lines, 'Tags', tag_id, f'0,{name} 1,{trigger_id}')
        trigger_ids.append(trigger_id)
    return trigger_ids[0]


